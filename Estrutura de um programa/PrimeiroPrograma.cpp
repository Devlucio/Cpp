//Programa em C++
#include <iostream>
using namespace std;
/*
  int-> é o tipo de retorno do valor da função,
  void-> informa que a função main() não ppossui definido nenhum parâmetro conhecido,
  main()-> é a primeira e principal função a ser executada,
  {-> inicia ou abre o corpo da função(programa),
  return 0-> O valor de retorno da função,
  }-> finaliza a função.
*/
int main (void){
    cout << "Oi mundo" << endl;
    system ("Pause");
    return 0;
}